// 1. What is HTTP?
// HTTP stands for "Hypertext Transfer Protocol." HTTP is the protocol used to transfer data over the web. It is part of the Internet protocol suite and defines commands and services used for transmitting webpage data. HTTP uses a server-client model

// 2. What is a URL?
// A Uniform Resource Locator, aka web address, is a reference to a web resource that specifies its location on a computer network and a mechanism for retrieving it. 

// 3. What is IP?
// An Internet Protocol address is a numerical label assigned to each device connected to a computer network that uses the Internet Protocol for communication. An IP address serves two main functions: host or network interface identification and location addressing.

// 4. What is a client?
// A client is a computer that connects to and uses the resources of a remote computer, or server

// 5. What is a server?
// A server is a piece of computer hardware or software (computer program) that provides functionality for other programs or devices, called "clients".

// 6. What is an HTTP request?
// An HTTP request method indicates the desired action to be performed for a given resource. For example GET, HEAD, POST, PUT, PATCH, DELETE

// 7. What is an HTTP response?
// What the server sends back after interpreting a request message, it includes
// Status-line
// Headers
// Optionally a response-body